<script>
$(document).ready(function () {
  $.ajax({
    url: "/get-cart-items",
    method: "GET",
    success: function (response) {
        let cartList = $('#cart-item-list');
        cartList.empty();

        // Update cart count in My Cart
        $(".my-cart-count").text(`(${response.count})`);

        // Update subtotal
        $("#cart-subtotal").text(`$${response.subtotal.toFixed(2)}`);

        if (response.items.length === 0) {
            cartList.append(`<li><p>No items in cart.</p></li>`);
        }

        response.items.forEach(function (item) {
            let product = item.product;

            cartList.append(`
                <li>
                    <a href="/product/${product.slug}" class="cart_img">
                        <img src="/uploads/singleProduct/${product.image}" alt="${product.name}" class="img-fluid w-100">
                    </a>
                    <div class="cart_text">
                        <a class="cart_title" href="/product/${product.slug}">${product.name}</a>
                        <p>₹${product.dp_price} <del>₹${product.price}</del></p>
                         <span><b>Color:</b> 
                            <div style="display:inline-block; width:15px; height:15px; background-color:${item.color}; border-radius:50%; margin-left:5px;"></div>
                        </span><br>
                        <span><b>Size:</b> ${item.size}</span>
                    </div>
                     <a class="del_icon" href="javascript:void(0);" onclick="removeCartItem(${item.id}, this)">
                        <i class="fal fa-times"></i>
                    </a>
                </li>
            `);
        });
    }
});


});



function removeCartItem(id, element) {
    fetch('/remove-cart-item', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
            'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
        },
        body: JSON.stringify({ id: id })
    })
    .then(response => response.json())
    .then(data => {
        if (data.success) {
            const li = element.closest('li');
            li.remove();
            updateCartCount();
        } else {
            // alert("Failed to/ remove item.");
        }
    })
    .catch(error => {
        console.error('Error:', error);
        // alert("Error occurred while removing item.");
    });
}





